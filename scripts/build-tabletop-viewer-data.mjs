#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const repoRoot = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const base = path.join(repoRoot, "examples/e2e-tabletop");
const files = [
  "executable-scenarios.json", "executable-scenario-002.json", "executable-scenario-004-incident.json",
  "domain-payloads-canonical.json", "domain-payloads-001.json", "domain-payloads-002.json", "domain-payloads-003.json",
  "domain-payloads-004.json", "domain-payloads-004-incident.json", "domain-payloads-fixes.json", "domain-payloads-task-events.json",
  "sequence-requirements.json"
];
const documents = files.map((name) => [name, JSON.parse(fs.readFileSync(path.join(base, name), "utf8"))]);
const output = `// Generated by scripts/build-tabletop-viewer-data.mjs\nwindow.TABLETOP_EMBEDDED=${JSON.stringify(documents)};\n`;
fs.writeFileSync(path.join(base, "viewer-data.js"), output);
console.log(`Wrote viewer-data.js with ${documents.length} documents`);
